package cz.cuni.mff.d3s.been.logging;

/**
 * BEEN log level numbering
 * 
 * @author darklight
 */
public enum LogLevel {
	/** Not used, to set TRACE.ordinal() to 1 */
	DUMMY,

	/** TRACE log level */
	TRACE,

	/** DEBUG log level */
	DEBUG,

	/** INFO log level */
	INFO,

	/** WARN log level */
	WARN,

	/** ERROR log level */
	ERROR
}
