package cz.cuni.mff.been.clinterface;

import java.io.IOException;

/**
 * This exception wraps IOExceptions that occur when a module sends its output.
 * 
 * @author Andrej Podzimek
 */
public final class ModuleOutputException extends CommandLineException {

	private static final long serialVersionUID = 124866882912121235L;

	/**
	 * The one and only constructor.
	 * 
	 * @param cause The wrapped IOException.
	 */
	public ModuleOutputException( IOException cause ) {
		super( cause );
	}
}
