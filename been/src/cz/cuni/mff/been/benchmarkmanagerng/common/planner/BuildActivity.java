package cz.cuni.mff.been.benchmarkmanagerng.common.planner;

import java.util.ArrayList;
import java.util.List;

/** 
 * Abstract representation for Build-Run workflow in benchmark.
 * Object of this class is intended to be generated by DBRPlanner
 * and stores sufficient information for generator to be 
 * able to generate tasks that will fulfill activity described by 
 * this object.
 * 
 * Build activity can contain one or more run activities.
 * This relation describes that run activities are dependent on
 * given build activity.
 *   
 * @author Jan Tattermusch
 *
 */
public class BuildActivity {

	/**
	 * Constructs new instance of build activity,
	 * based on software version and build number.
	 * 
	 * @param version software version
	 * @param buildNumber build number
	 */
	public BuildActivity(String version, int buildNumber) {
		super();
		this.version = version;
		this.buildNumber = buildNumber;
	}

	private String version;
	private int buildNumber;
	private List<RunActivity> runActivities = new ArrayList<RunActivity>();

	/**
	 * 
	 * @return version that should be built by this activity
	 */
	public String getVersion() {
		return version;
	}

	/**
	 * 
	 * @return build number of this activity
	 */
	public int getBuildNumber() {
		return buildNumber;
	}

	/**
	 * 
	 * @return list of dependent run activities
	 */
	public List<RunActivity> getRunActivities() {
		return runActivities;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + buildNumber;
		result = prime * result
				+ ((runActivities == null) ? 0 : runActivities.hashCode());
		result = prime * result + ((version == null) ? 0 : version.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		BuildActivity other = (BuildActivity) obj;
		if (buildNumber != other.buildNumber)
			return false;
		if (runActivities == null) {
			if (other.runActivities != null)
				return false;
		} else if (!runActivities.equals(other.runActivities))
			return false;
		if (version == null) {
			if (other.version != null)
				return false;
		} else if (!version.equals(other.version))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "buildActivity(" + version + ", " + buildNumber + "," + runActivities + ")"; 
	}
}